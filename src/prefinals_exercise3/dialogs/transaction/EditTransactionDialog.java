    private void saveChangesButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveChangesButtonActionPerformed
        String selectedItem = (String) cmbItem.getSelectedItem();
        String quantityStr = quantityField.getText().trim();
        String transactionType = (String) cmbTransactionType.getSelectedItem();
        String notes = notesField.getText().trim();

        if (selectedItem == null || quantityStr.isEmpty() || transactionType == null) {
            JOptionPane.showMessageDialog(this, "Please fill out all the necessary fields.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        try {
            int newQuantity = Integer.parseInt(quantityStr);

            try (Connection con = getConnection()) {
                con.setAutoCommit(false);

                // Retrieve the original transaction details
                String originalItemName = null;
                String originalTransactionType = null;
                int originalQuantity = 0;
                try (PreparedStatement selectStmt = con.prepareStatement("SELECT i.item_name, t.transaction_type, t.quantity FROM Transactions t JOIN Items i ON t.item_id = i.item_id WHERE t.transaction_id = ?")) {
                    selectStmt.setInt(1, this.id);
                    ResultSet rs = selectStmt.executeQuery();
                    if (rs.next()) {
                        originalItemName = rs.getString("item_name");
                        originalTransactionType = rs.getString("transaction_type");
                        originalQuantity = rs.getInt("quantity");
                    }
                }

                // Update item quantity
                if (!updateItemQuantity(originalItemName, originalQuantity, originalTransactionType, newQuantity, transactionType)) {
                    JOptionPane.showMessageDialog(this, "New quantity would result to a negative stock value.", "Error", JOptionPane.ERROR_MESSAGE);
                    con.rollback();
                    return;
                }

                // Update transaction
                try (PreparedStatement pstmt = con.prepareStatement("UPDATE Transactions SET item_id = (SELECT item_id FROM Items WHERE item_name = ?), quantity = ?, transaction_type = ?, notes = ? WHERE transaction_id = ?")) {
                    pstmt.setString(1, selectedItem);
                    pstmt.setInt(2, newQuantity);
                    pstmt.setString(3, transactionType);
                    pstmt.setString(4, notes);
                    pstmt.setInt(5, this.id);
                    pstmt.executeUpdate();
                }

                con.commit(); // Commit transaction
                dispose();
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Please enter a valid number for quantity.", "Error", JOptionPane.ERROR_MESSAGE);
        } catch (SQLException | ClassNotFoundException e) {
            showError(e);
        }
    }//GEN-LAST:event_saveChangesButtonActionPerformed

