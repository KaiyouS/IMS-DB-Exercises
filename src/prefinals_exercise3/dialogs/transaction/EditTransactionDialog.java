    private void loadItems(String selectedItem) {
        try (Connection con = getConnection();
             Statement stmt = con.createStatement();
             ResultSet rs = stmt.executeQuery("SELECT item_name FROM Items")) {

            DefaultComboBoxModel<String> model = new DefaultComboBoxModel<>();
            while (rs.next()) {
                String itemName = rs.getString("item_name");
                model.addElement(itemName);
            }
            cmbItem.setModel(model);
            cmbItem.setSelectedItem(selectedItem);
        } catch (SQLException | ClassNotFoundException e) {
            showError(e);
        }
    }
    
    private boolean updateItemQuantity(String itemName, int oldQuantity, String oldType, int newQuantity, String newType) {
        int oldValue = 0;
        try (Connection con = getConnection();
             PreparedStatement selectStmt = con.prepareStatement("SELECT quantity_on_hand FROM Items WHERE item_name = ?")) {
            selectStmt.setString(1, itemName);
            ResultSet rs = selectStmt.executeQuery();
            
            if (rs.next()) {
                oldValue = rs.getInt("quantity_on_hand");
            }
            
            // Calculate the old value based on the original transaction
            if (oldType.equalsIgnoreCase("Stock in")) {
                oldValue -= oldQuantity;
            } else if (oldType.equalsIgnoreCase("Stock out")) {
                oldValue += oldQuantity;
            }
            
            // Calculate the new value based on the new transaction
            int newValue = oldValue;
            if (newType.equalsIgnoreCase("Stock in")) {
                newValue += newQuantity;
            } else if (newType.equalsIgnoreCase("Stock out")) {
                newValue -= newQuantity;
            }
            
            if (newValue < 0) {
                return false;
            }
            
            try (PreparedStatement updateStmt = con.prepareStatement("UPDATE Items SET quantity_on_hand = ? WHERE item_name = ?")) {
                updateStmt.setInt(1, newValue);
                updateStmt.setString(2, itemName);
                updateStmt.executeUpdate();
            }
            
        } catch (SQLException | ClassNotFoundException e) {
            showError(e);
            return false;
        }
        
        return true;
    }
    
